# computational_phase_transition.py
"""
Computational Phase Transitions in the Silicon Manifold.

Formalizes the mapping from silicon state coordinates S = (n, d, ℓ, κ)
to computational complexity classes. Detects phase boundaries where
the set of physically realizable computations changes discontinuously.
"""

import numpy as np
from typing import Dict, List, Tuple, Optional, Set
from dataclasses import dataclass, field
from enum import Enum, auto
from scipy.special import expit  # sigmoid


# ─── Computational Complexity Classes ───

class ComputationalClass(Enum):
    """Computational complexity classes realizable in physical substrates."""
    CLASSICAL_P = auto()           # Deterministic polynomial time (NAND-based)
    QUANTUM_BQP = auto()           # Bounded-error quantum polynomial
    STOCHASTIC_BPP = auto()        # Bounded-error probabilistic polynomial
    CONTINUOUS_VARIABLE = auto()   # Continuous-variable computation (photonic)
    DEGRADED_NC0 = auto()          # Constant-depth with noise (defect-limited)
    MIXED_BQP_BPP = auto()         # Crossover regime (partial coherence)
    CHAOTIC = auto()               # No structured computation possible


@dataclass
class ComputationalGateSet:
    """The set of primitive operations a physical regime can implement."""
    name: str
    gates: Set[str]
    is_universal_classical: bool = False
    is_universal_quantum: bool = False
    max_circuit_depth: float = float('inf')
    error_rate: float = 0.0
    
    def closure_dimension(self) -> int:
        """
        Dimension of the function space generated by this gate set.
        Classical NAND: countable (all Boolean functions)
        Quantum {H, CNOT, T}: uncountable (all unitaries on 2^n states)
        """
        if self.is_universal_quantum:
            return float('inf')  # uncountably infinite
        elif self.is_universal_classical:
            return float('inf')  # countably infinite
        else:
            return len(self.gates) ** self.max_circuit_depth


